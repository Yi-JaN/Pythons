def gcd(x,y) :
    while y>=1 : #這裡開始輾轉相除法
        xx=x%y
        x=y
        y=xx
    return x    #最後y如果沒有>=1就往下來到這行然後return不下函數名稱丟回去print括號裡
                #然後沒下函數名稱該回傳值丟回去,就無法由下至上再度丟入def遞迴函數函數裡
x=int(input()) #輸入兩個值
y=int(input())
print(gcd(x,y)) #把兩個值丟入gcd函數名稱,之後由下至上函數名稱對函數名稱丟進去做def遞迴函數

