#   x=91 y=49      42  推過去後-> x=49 y=42
#   x=49 y=42       7  推過去後-> x=42 y=7
#   x=42 y=7        0  推過去後-> x=7  y=0
#   x=7  y=0  ->之後y=0代表已經最終點,一但輾轉相除法的y是0就不用在執行輾轉相除法
#               之後直接取第一個值,也就是x去做他們彼此之間的最大公因數,所以retrun
#               x這邊,是return他們之間的最大公因數回傳回去!
