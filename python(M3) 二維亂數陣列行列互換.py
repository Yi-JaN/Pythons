#補充(seed()的意思)-> seed()用於設定隨機數種子，一個特定的種子可以產生一個
#                    特定的偽隨機序列，這個函數的主要目的，是讓你的模擬能夠可
#                    "重複出現"，因為很多時候我們需要取隨機數，但這段代碼(我的n)
#                    再跑一次的時候，結果就"不一樣"了，"如果需要重複出現同樣的
#                    模擬結果的話"，"就可以用seed()"  

#補充->data二維陣列跟seed的關係如下說明:
#data這邊要用"random"取10*10的隨機亂數給n,因為當初n丟到random裡的seed,所以設一個data
#二維陣列,利用他用"random"去取10*10的隨機亂數值,並且用seed保持這10*10的隨機亂數值!之後
#每一個隨機亂數值從高0值0開始到高9值9,也就是所謂10*10全部的值都會保存起來丟給seed()裡的
#n,所以只要代碼(n)輸入,就會依程式要求列出10*10陣列保存起來的亂數值

