import random
n=int(input()) #輸入一個正整數n->(我的代碼)
random.seed(n) #把代碼丟入random裡的seed括號裡,詳請請看下方補充(seed()的意思)
data=[[0]*10 for o in range(10)] #data二維陣列負責取這段代碼的10*10矩陣,用亂數取值
for i in range(0,10,1) : #這裡是一開始先亂數取值,先亂數取值然後再丟給seed之後再針對目前高i值j位置值上丟過去
    for j in range(0,10,1) :#for i為高,for j為值
        data[i][j]=random.randint(0,9) #每循環1次亂數取值0~9,然後保存給seed後再丟給目前data的高i值j位置上
for a in range(0,10,1) :#這裡是負責列印,然後要側翼取值列印,所以for a先假設為高
    for b in range(0,10,1) : #for b先假設為值
        print(data[b][a],end="\t") #每循環1次來到這邊,b就變成高,a就變成值,然後針對目前高b值a位置上取值然後再"\t"(tab)
    print() #來到這邊就換行,換下一行列印

